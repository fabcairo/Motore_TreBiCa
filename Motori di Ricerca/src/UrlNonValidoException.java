
public class UrlNonValidoException extends Exception {

	public UrlNonValidoException() {
	}

	public UrlNonValidoException(String message) {
		super(message);
	}

	public UrlNonValidoException(Throwable cause) {
		super(cause);
	}

	public UrlNonValidoException(String message, Throwable cause) {
		super(message, cause);
	}

	public UrlNonValidoException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}