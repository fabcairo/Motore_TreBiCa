
public class ProtocolloNonValidoException extends UrlNonValidoException {

	public ProtocolloNonValidoException() {
	}

	public ProtocolloNonValidoException(String message) {
		super(message);
	}

	public ProtocolloNonValidoException(Throwable cause) {
		super(cause);
	}

	public ProtocolloNonValidoException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProtocolloNonValidoException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
